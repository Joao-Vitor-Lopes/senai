#Crie um programa que peça dois números inteiros e imprima a soma deles.

numero_inteiro = int(input("Digite um número inteiro: "))
numero_inteiro2 = int(input("Digite mais um número inteiro: "))

soma = numero_inteiro + numero_inteiro2
print(f"A soma dos números é: {soma}")

#Escreva um código que solicite um número decimal e imprima o seu dobro.

numero_decimal = float(input("Digite um número decimal para dobrarmos: "))
dobrar = numero_decimal * 2
print(f"O dobro do número é: {dobrar}")

